def calc(_num1, _num2):
    return _num1 + _num2

def subt(_num1, _num2):
    return _num1 - _num2

def mult(_num1, _num2):
    return _num1 * _num2

def div(_num1, _num2):
    return float(_num1) / _num2

print(calc(10, 20))
print(subt(10, 20))
print(mult(10, 20))
print(div(10, 20))
